package net.foxgenesis.util.resource;

/**
 * Enumeration of all supported configuration file types.
 * 
 * @author Ashley
 * 
 * @since 1.1.1
 */
public enum ConfigType {

	/**
	 * Type representing {@code .properties} files.
	 */
	PROPERTIES,
	/**
	 * Type representing {@code .ini} files.
	 */
	INI,
	/**
	 * Type representing {@code .json} files.
	 */
	JSON,
	/**
	 * Type representing {@code .xml} files.
	 */
	XML
}
